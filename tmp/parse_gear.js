const parseCSV = (lines) => {
    const result = {};
    
    lines.forEach(line => {
        const parts = line.split(',');
        if (parts.length < 10) return;
        
        const [category, slot, name, tier, rarity, ip, damage, defense, hp, speed] = parts;
        
        const trimmedName = name.trim();
        if (!result[trimmedName]) result[trimmedName] = {};
        if (!result[trimmedName][tier]) result[trimmedName][tier] = {};
        
        const qualityMap = { 'Normal': 0, 'Good': 1, 'Outstanding': 2, 'Excellent': 3, 'Masterpiece': 4 };
        const qId = qualityMap[rarity.trim()];
        
        if (qId === undefined) return;

        const stats = {};
        const dmgVal = parseFloat(damage);
        const defVal = parseFloat(defense);
        const hpVal = parseFloat(hp);
        const spdVal = parseFloat(speed.replace('%', ''));
        
        if (dmgVal > 0) stats.damage = dmgVal;
        if (defVal > 0) stats.defense = defVal;
        if (hpVal > 0) stats.hp = hpVal;
        if (spdVal > 0) stats.speed = spdVal;
        stats.ip = parseInt(ip);
        
        result[trimmedName][tier][qId] = stats;
    });
    
    return result;
};

